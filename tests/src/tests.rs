fn assert_script_error(err: Error, err_code: i8) {
    let error_string = err.to_string();
    assert!(
        error_string.contains(format!("error code {} ", err_code).as_str()),
        "error_string: {}, expected_error_code: {}",
        error_string,
        err_code
    );
}

#[test]
fn test_signature() {
    // This tests the interoperability between the on-chain signature verification
    // and the key generation & signing in the perun-ckb-backend's wallet.

    // This signature was generated by the wallet in the perun-ckb-backend
    let sig_string = "304402200483fd652b37e673183f9815e29497d4973eafd6c85e7029f1c3fc42387eb94b0220598f87fac0bb4e072d791449303d413365fec89b21c268556009b4a11933e8a8";
    let sig = hex::decode(sig_string).expect("decoding signature");
    let sig_bytes: Bytes = sig.into();

    // This public key was generated by the wallet in the perun-ckb-backend
    let pubkey_string = "04be06fb46564dee6c1320c43dd612088fa4e64ff737364345b94ec336cee66be4a7a59aad28310068deae80a16ec26e817d357f3897a360e22eab6019d2a8d86b";
    let pubkey = hex::decode(pubkey_string).expect("decoding pubkey");
    let pubkey_bytes: [Byte;65] = pubkey.iter().map(|x| (*x).into()).collect::<Vec<Byte>>().try_into().unwrap();
    SEC1EncodedPubKey::new_builder().set(pubkey_bytes).build();

    let balances_array: [Uint128; 2] = [10u128.pack(), 11u128.pack()];
    let balances = Balances::new_builder().set(balances_array).build();
    let channel_state = ChannelState::new_builder()
    .channel_id(Byte32::zero())
    .balances(balances)
    .is_final(Bool::from_bool(true))
    .version(10u64.pack())
    .build();
    let msg = channel_state.as_slice();

    verify_signature(msg, &sig_bytes, pubkey.as_slice()).expect("valid signature");
}

#[test]
fn test_success() {
    // Deploy contracts into environment.
    let mut context = Context::default();
    let pe = perun::harness::Env::new(&mut context).expect("preparing environment");

    // Prepare cells.
    let input_out_point = context.create_cell(
        CellOutput::new_builder()
            .capacity(1000u64.pack())
            .lock(pe.always_success_script.clone())
            .build(),
        Bytes::new(),
    );

    // Prepare transaction fields.
    let input = CellInput::new_builder()
        .previous_output(input_out_point)
        .build();
    let outputs = vec![
        CellOutput::new_builder()
            .capacity(500u64.pack())
            .lock(pe.always_success_script.clone())
            .build(),
        CellOutput::new_builder()
            .capacity(500u64.pack())
            .lock(pe.always_success_script.clone())
            .build(),
    ];

    let outputs_data = vec![Bytes::new(); 2];

    // Build transaction.
    let tx = TransactionBuilder::default()
        .input(input)
        .outputs(outputs)
        .outputs_data(outputs_data.pack())
        .cell_dep(pe.always_success_script_dep)
        .build();
    let tx = context.complete_tx(tx);

    // Run transaction.
    let cycles = context
        .verify_tx(&tx, MAX_CYCLES)
        .expect("pass verification");
    println!("consume cycles: {}", cycles);
}

#[test]
fn channel_test_bench() {
    [
        test_funding_abort,
        test_multiple_disputes,
        test_successful_funding,
    ]
    .iter()
    .for_each(|test| {
        let mut context = Context::default();
        let pe = perun::harness::Env::new(&mut context).expect("preparing environment");
        test(&mut context, &pe);
    });
}

fn create_channel_test<P>(
    context: &mut Context,
    env: &perun::harness::Env,
    parts: &[P],
    test: impl Fn(&mut perun::channel::Channel<P, perun::State>) -> Result<(), perun::Error>,
) where
    P: Eq + std::hash::Hash + Copy + Clone,
{
    let mut chan = perun::channel::Channel::new(context, env, parts);
    test(&mut chan);
}

fn test_funding_abort(context: &mut Context, env: &perun::harness::Env) {
    let parts @ [alice, bob] = ["alice", "bob"];
    let funding_timeout = 10;
    let funding_agreement = test::FundingAgreement::new(parts.len());
    create_channel_test(context, env, &parts, |chan| {
        chan.with(alice)
            .open(&funding_agreement)
            .expect("opening channel");

        chan.delay(funding_timeout);

        chan.with(bob)
            .invalid()
            .fund(&funding_agreement)
            .expect("invalid funding channel");

        chan.with(alice).abort().expect("aborting channel");

        chan.assert();
        Ok(())
    });
}

fn test_successful_funding(context: &mut Context, env: &perun::harness::Env) {
    let parts @ [alice, bob] = ["alice", "bob"];
    let funding_agreement = test::FundingAgreement::new(parts.len());
    create_channel_test(context, env, &parts, |chan| {
        chan.with(alice)
            .open(&funding_agreement)
            .expect("opening channel");

        chan.with(bob)
            .fund(&funding_agreement)
            .expect("funding channel");

        chan.assert();
        Ok(())
    });
}

fn test_multiple_disputes(context: &mut Context, env: &perun::harness::Env) {
    let parts @ [alice, bob] = ["alice", "bob"];
    let funding_agreement = test::FundingAgreement::new(parts.len());
    create_channel_test(context, env, &parts, |chan| {
        chan.with(alice)
            .open(&funding_agreement)
            .expect("opening channel");

        chan.with(bob)
            .fund(&funding_agreement)
            .expect("funding channel");

        chan.with(alice)
            .valid()
            .dispute()
            .expect("disputing channel");

        chan.with(bob).valid().dispute().expect("disputing channel");

        chan.with(alice)
            .invalid()
            .dispute()
            .expect("disputing channel");

        chan.assert();
        Ok(())
    });
}
